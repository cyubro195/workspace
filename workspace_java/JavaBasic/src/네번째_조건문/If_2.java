package 네번째_조건문;

public class If_2 {
    public static void main(String[] args) {
        System.out.println("프로그램 시작");

        int x = 5;

        //x의 값이 7이면 '1' 출력, 그렇지 않으면 '2'를 출력
        //같냐는 ==, if = 만약에, else = 그 밖에&그렇지 않으면
        if (x == 7) {
            System.out.println(1);
        }
        else {
            System.out.println(2);
        }

        System.out.println("프로그램 종료");
    }
}
