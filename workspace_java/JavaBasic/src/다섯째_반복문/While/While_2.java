package 다섯째_반복문.While;

public class While_2 {
    public static void main(String[] args) {
        // 1부터 10까지 출력
        int i = 1;

        while (i <= 10){ // i가 10보다 작은동안 반복할거라는 뜻
            // System.out.println(1); > 1이 10개 나옴
            System.out.println(i); // > 숫자로 나옴
            i++; // i값을 1씩 증가하게한다.

        }
    }
}
