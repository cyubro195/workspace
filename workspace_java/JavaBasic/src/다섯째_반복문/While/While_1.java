package 다섯째_반복문.While;

/*
    while(반복조건){
        반복적으로 실행되는 내용
    }
*/

public class While_1 {
    public static void main(String[] args) {

        // 반복 시작점
        int i = 1; // 1부터 시작하겠습니다라고 시작점을 정한다!
        while(i <= 3) { //반복 조건

            System.out.println(1);
            i++; // 반복을 파기할 조건

        }
    }
}
